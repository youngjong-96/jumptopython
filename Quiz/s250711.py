# 비트연산자: &, |, ^, ~, <<, >>
a,b=2,3    # 0010, 0011
print(a&b) # 모두 1이면 1, 아니면 0 -> 0010
print(a|b) # 모두 0이면 0, 아니면 1 -> 0011
print(a^b) # 같으면 0, 다르면 1 -> 0001
print(~a)  # 0을 1로, 1을 0으로 -> 1101
